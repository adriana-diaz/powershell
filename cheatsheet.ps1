$a = 2                                                    # variable basica
$a += 1                                                   # suma
$a -= 1                                                   # resta

$a -eq 0                                                  # Operador igual
$a -ne 5                                                  # Operador distinto que
$a -gt 2                                                  # Operador mayor que
$a -lt 3                                                  # Operador menor que

$FirstName = "Adriana"
$FirstName -like "A"                                      # Operador para comparar strings

